function applyPaaxioTranslation(lang) {
  const t = translations[lang];
  if (!t) return;

  // <html lang>
  document.documentElement.lang = lang;

  // Mise à jour du label visible dans le globe
  const langCurrentEl = document.getElementById("langCurrent");
  const labels = { fr: "FR", en: "EN", es: "ES" };
  if (langCurrentEl) langCurrentEl.textContent = labels[lang] || lang.toUpperCase();

  // Rétrocompatibilité translateBtn (pages projet)
  const btn = document.getElementById("translateBtn");
  if (btn) btn.innerHTML = t.btnLabel;

  const navItems = document.querySelectorAll("nav ul li a");
  if (navItems[0]) navItems[0].innerHTML = t.navAbout;
  if (navItems[1]) navItems[1].innerHTML = t.navProjects;
  if (navItems[2]) navItems[2].innerHTML = t.navSkills;
  if (navItems[3]) navItems[3].innerHTML = t.navContact;

  const backLink = document.querySelector(".back-link");
  if (backLink) backLink.innerHTML = t.backLink;

  const h2 = document.querySelector("#project-detail h2");
  if (h2) h2.innerHTML = t.paaxioTitle;

  const h3s = document.querySelectorAll("#project-detail h3");
  if (h3s[0]) h3s[0].innerHTML = t.paaxioSubtitle;
  if (h3s[1]) h3s[1].innerHTML = t.paaxioTitleDesc;
  if (h3s[2]) h3s[2].innerHTML = t.paaxioConcept;
  if (h3s[3]) h3s[3].innerHTML = t.paaxioPhase1;
  if (h3s[4]) h3s[4].innerHTML = t.paaxioPhase2;
  if (h3s[5]) h3s[5].innerHTML = t.paaxioObjectives;
  if (h3s[6]) h3s[6].innerHTML = t.paaxioTechs;
  if (h3s[7]) h3s[7].innerHTML = t.paaxioStrengths;
  if (h3s[8]) h3s[8].innerHTML = t.paaxioLearnings;
  if (h3s[9]) h3s[9].innerHTML = t.paaxioProjectStatus;

  const desc = document.querySelector(".project-description");
  if (desc) desc.innerHTML = t.paaxioDesc;

  const concept = document.querySelector(".project-concept");
  if (concept) concept.innerHTML = t.paaxioConceptDesc;

  const phase1List = document.querySelector(".phase-info ul");
  if (phase1List) phase1List.innerHTML = `
    <li>${t.paaxioPhase1li1}</li><li>${t.paaxioPhase1li2}</li>
    <li>${t.paaxioPhase1li3}</li><li>${t.paaxioPhase1li4}</li>
    <li>${t.paaxioPhase1li5}</li>`;

  const phase2 = document.querySelector(".phase-info-2");
  if (phase2) phase2.innerHTML = t.paaxioPhase2Desc;

  const objectives = document.querySelector(".objectives-list");
  if (objectives) objectives.innerHTML = `
    <li>${t.paaxioObjective1}</li><li>${t.paaxioObjective2}</li>
    <li>${t.paaxioObjective3}</li><li>${t.paaxioObjective4}</li>
    <li>${t.paaxioObjective5}</li><li>${t.paaxioObjective6}</li>`;

  const strengths = document.querySelector(".strengths-list");
  if (strengths) strengths.innerHTML = `
    <li>${t.paaxioStrength1}</li><li>${t.paaxioStrength2}</li>
    <li>${t.paaxioStrength3}</li><li>${t.paaxioStrength4}</li>`;

  const learnings = document.querySelector(".learnings-list");
  if (learnings) learnings.innerHTML = `
    <li>${t.paaxioLearnings1}</li><li>${t.paaxioLearnings2}</li>
    <li>${t.paaxioLearnings3}</li><li>${t.paaxioLearnings4}</li>
    <li>${t.paaxioLearnings5}</li>`;

  const status = document.querySelector(".project-status");
  if (status) status.innerHTML = t.paaxioProjectStatusDesc;

  const ghBtn = document.querySelector(".project-links .btn-github");
  if (ghBtn) ghBtn.innerHTML = t.paaxioBtnGitHub;

  const footerP = document.querySelector("footer p");
  if (footerP && t.footerText) footerP.innerHTML = t.footerText;
}

// ── Init immédiate + bouton ───────────────────────────────────────────────────
let currentLang = window.SITE_LANG || "fr";

applyPaaxioTranslation(currentLang);
if (typeof updateWidgetLanguage === "function") updateWidgetLanguage(currentLang);

 // Révéler la page (supprime le masque posé par lang.js)
  if (typeof window.revealPage === "function") window.revealPage();

  // ── Sélecteur de langue (globe dropdown) ─────────────────────────────────
  const langSwitcher  = document.getElementById("langSwitcher");
  const langGlobeBtn  = document.getElementById("langGlobeBtn");
  const langDropdown  = document.getElementById("langDropdown");
  const langCurrent   = document.getElementById("langCurrent");
  const langOptions   = document.querySelectorAll(".lang-option");

  const langLabels = { fr: "FR", en: "EN", es: "ES" };

  function setActiveLangOption(lang) {
    langOptions.forEach(btn => {
      btn.classList.toggle("active", btn.dataset.lang === lang);
    });
    if (langCurrent) langCurrent.textContent = langLabels[lang] || lang.toUpperCase();
  }

  // Init visuelle
  setActiveLangOption(currentLang);

  if (langGlobeBtn && langDropdown) {
    // Ouvrir / fermer au clic sur le globe
    langGlobeBtn.addEventListener("click", e => {
      e.stopPropagation();
      langSwitcher.classList.toggle("open");
    });

    // Fermer en cliquant ailleurs
    document.addEventListener("click", e => {
      if (!langSwitcher.contains(e.target)) langSwitcher.classList.remove("open");
    });

    // Choisir une langue
    langOptions.forEach(btn => {
      btn.addEventListener("click", () => {
        const lang = btn.dataset.lang;
        currentLang = lang;
        localStorage.setItem("language", lang);
        applyPaaxioTranslation(lang);
        updateWidgetLanguage(lang);
        setActiveLangOption(lang);
        langSwitcher.classList.remove("open");
      });
    });
  }
